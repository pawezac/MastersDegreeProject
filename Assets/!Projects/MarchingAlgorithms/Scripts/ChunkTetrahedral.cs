    public float iso = 0.5f; // Poziom izo-powierzchni

    int getIdxFromCoords(int x, int y, int z)
    {
        return x + GridMetrics.PointsPerChunk(0) * (y + GridMetrics.PointsPerChunk(0) * z);
    }

    int[,] combinationTable = new int[6, 4]
    {
        {0,2,3,7},
        {0,2,6,7},
        {0,4,6,7 },
        {0,6,1,2 },
        {0,6,1,4 },
